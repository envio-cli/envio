_envio() {
    local i cur prev opts cmd
    COMPREPLY=()
    cur="${COMP_WORDS[COMP_CWORD]}"
    prev="${COMP_WORDS[COMP_CWORD-1]}"
    cmd=""
    opts=""

    for i in ${COMP_WORDS[@]}
    do
        case "${cmd},${i}" in
            ",$1")
                cmd="envio"
                ;;
            envio,export)
                cmd="envio__export"
                ;;
            envio,help)
                cmd="envio__help"
                ;;
            envio,import)
                cmd="envio__import"
                ;;
            envio,load)
                cmd="envio__load"
                ;;
            envio,profile)
                cmd="envio__profile"
                ;;
            envio,run)
                cmd="envio__run"
                ;;
            envio,set)
                cmd="envio__set"
                ;;
            envio,unload)
                cmd="envio__unload"
                ;;
            envio,unset)
                cmd="envio__unset"
                ;;
            envio,version)
                cmd="envio__version"
                ;;
            envio__help,export)
                cmd="envio__help__export"
                ;;
            envio__help,help)
                cmd="envio__help__help"
                ;;
            envio__help,import)
                cmd="envio__help__import"
                ;;
            envio__help,load)
